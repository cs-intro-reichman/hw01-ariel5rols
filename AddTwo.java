/*
 * Adds two given integers and prints the result in a fancy way.
 */
public class AddTwo {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 7;
        int sum = num1 + num2;

        // Print the result in a fancy way
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}

